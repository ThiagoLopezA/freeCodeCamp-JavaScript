// Explore Differences Between the var and let Keywords

/*
If u use the 'var' keywords when u defines a variable
u can see things like:
    var name = 'MyName';
    var name = 'YourName';
And that isn't going to be an error, but in another hand
if u use let to define the name variable it's going to 
appear an error, because you can't declare two times
the same thing.
So let can be used to declare once and var can make troubles
declaring variable more than one time.
*/

/*
Before
var catName = "Oliver";
var catSound = "Meow!";
*/

// After

let catName = "Oliver";
let catSound = "Meow!";
